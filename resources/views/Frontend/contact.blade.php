<script>
    document.addEventListener("DOMContentLoaded", function () {
      const form = document.getElementById("contactForm");

      // ✅ Regex patterns
      const namePattern = /^[A-Za-z\s]+$/; // only letters and spaces
      const emailPattern = /^[^\s@]+@[^\s@]+\.[^\s@]+$/; // standard email format
      const subjectPattern = /^[A-Za-z0-9\s.,!?'-]{2,100}$/; // letters, numbers, punctuation

      function validateField(input) {
        const value = input.value.trim();
        let isValid = true;

        switch (input.name) {
          case "name":
            isValid = namePattern.test(value);
            break;

          case "email":
            isValid = emailPattern.test(value);
            break;

          case "subject":
            isValid = subjectPattern.test(value);
            break;

          case "message":
            isValid = value.length >= 10;
            break;
        }

        if (!isValid) {
          input.classList.add("is-invalid");
        } else {
          input.classList.remove("is-invalid");
        }

        return isValid;
      }

      // Real-time validation
      form.querySelectorAll("input, textarea").forEach((input) => {
        input.addEventListener("input", () => validateField(input));
        input.addEventListener("blur", () => validateField(input));
      });

      // Final validation before submission
      form.addEventListener("submit", function (e) {
        e.preventDefault();
        let allValid = true;

        form.querySelectorAll("input, textarea").forEach((input) => {
          if (!validateField(input)) allValid = false;
        });

        if (allValid) {
          // ✅ All fields valid, you can safely submit
          form.submit();
        }
      });
    });
  </script>








