    /*--------------------------------------------
      FRONTEND VALIDATION BEFORE FORM SUBMIT
    --------------------------------------------*/
    function validateForm() {
        let valid = true;

        // Clear all previous errors
        document.querySelectorAll(".error-msg").forEach(e => e.remove());

        // Validate each field
        Object.keys(requiredFields).forEach(fieldName => {

            let field = applyForm.querySelector(`[name="${fieldName}"]`);
            if (!field) return;

            let wrapper = field.closest(".col-md-6");

            // Create error box
            let err = document.createElement("div");
            err.className = "text-danger small error-msg";

            // Field-specific validation
            if (fieldName === "email") {
                let emailPattern = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
                if (!emailPattern.test(field.value.trim())) {
                    err.textContent = requiredFields[fieldName];
                    wrapper.appendChild(err);
                    valid = false;
                }
            } 
            else if (fieldName === "phone") {
                if (field.value.length !== 10 || isNaN(field.value)) {
                    err.textContent = requiredFields[fieldName];
                    wrapper.appendChild(err);
                    valid = false;
                }
            }
            else if (fieldName === "resume") {
                let file = field.files[0];
                if (!file) {
                    err.textContent = requiredFields[fieldName];
                    wrapper.appendChild(err);
                    valid = false;
                }
            }
            else {
                if (!field.value.trim()) {
                    err.textContent = requiredFields[fieldName];
                    wrapper.appendChild(err);
                    valid = false;
                }
            }
        });

        return valid;
    }

    /*--------------------------------------------
      FRONTEND FILE VALIDATION (Type + Size)
    --------------------------------------------*/
    resumeInput.addEventListener("change", function () {
        let wrapper = this.closest(".col-md-6");

        // Remove previous error
        let prev = wrapper.querySelector(".error-msg");
        if (prev) prev.remove();

        let file = this.files[0];
        if (!file) return;

        let err = document.createElement("div");
        err.className = "text-danger small error-msg";

        const allowed = [
            "application/pdf",
            "application/msword",
            "application/vnd.openxmlformats-officedocument.wordprocessingml.document"
        ];

        if (!allowed.includes(file.type)) {
            err.textContent = "Only PDF, DOC, DOCX allowed";
            wrapper.appendChild(err);
            this.value = "";
            return;
        }

        if (file.size > (3 * 1024 * 1024)) {
            err.textContent = "File size must be under 3MB";
            wrapper.appendChild(err);
            this.value = "";
            return;
        }
    });

